#define DEBUGPRINT2(...)       fprintf(stderr, __VA_ARGS__)
#define DEBUGPRINT3(...)       printf(__VA_ARGS__)
#define DEBUGPRINT(_fmt, ...)  DEBUGPRINT2(WHERESTR _fmt, WHEREARG, __VA_ARGS__)
#define DEBUGUARTPRINT(...)
#else
#define DEBUGPRINT(_fmt, ...)
#define DEBUGUARTPRINT(...)
#endif

#endif
